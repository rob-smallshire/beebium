// Reverse 6 bits: converts from visual format (bit 5 = left) to SAA5050 format (bit 0 = left)
inline constexpr uint8_t reverse_6bits(uint8_t v) {
    return static_cast<uint8_t>(
        ((v & 0x01) << 5) | ((v & 0x02) << 3) | ((v & 0x04) << 1) |
        ((v & 0x08) >> 1) | ((v & 0x10) >> 3) | ((v & 0x20) >> 5)
    );
}

// Pre-reversed font data for SAA5050 consumption (bit 0 = leftmost)
// Computed at compile time from TELETEXT_FONT_RAW
inline constexpr auto make_reversed_font() {
    std::array<std::array<uint8_t, 10>, 96> result{};
    for (int ch = 0; ch < 96; ++ch) {
        for (int row = 0; row < 10; ++row) {
            result[ch][row] = reverse_6bits(TELETEXT_FONT_RAW[ch][row]);
        }
    }
    return result;
}

inline constexpr auto TELETEXT_FONT_REVERSED = make_reversed_font();

